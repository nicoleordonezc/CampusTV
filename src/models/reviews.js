export class Reviews {
    constructor(userName, contentName, title, review, score, likes, dislikes, date) {

        this.userName = userName;
        this.contentName = contentName;
        this.title = title;
        this.review = review;
        this.score = score;
        this.likes = likes;
        this.dislikes = dislikes;
        this.date = date
    }
}